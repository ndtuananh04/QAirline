<section>
	<h2>Supervision Structure</h2>
	<p>
		To comply with The Securities Exchange of Thailand’s rules, the company structure is divided
		into two parts:
	</p>
	<h3>1. Company’s Committee</h3>
	<p>
		Responsible for advising and supervising the management division to follow policies. The
		committee consists of:
	</p>
	<ul>
		<li>Audit Committee</li>
		<li>Independent Committee</li>
		<li>Corporate Governance Committee</li>
		<li>Nomination, Remuneration, and Human Resource Development Committee</li>
		<li>Corporate Social Responsibility Committee</li>
		<li>Managing Director’s Performance Evaluating Committee</li>
		<li>Risk Management Committee</li>
		<li>Executive Management Committee</li>
		<li>Law Committee</li>
	</ul>
	<h3>2. Company’s Management Division</h3>
	<p>Divided into two parts:</p>
	<ul>
		<li>
			<strong>Airline Business:</strong> Includes commerce, strategic development, finance, human resources,
			and product lines, along with operations and technical units.
		</li>
		<li>
			<strong>Airline Supporting Business:</strong> Includes product commerce, airline kitchen, Thai
			Smile business, and ground business units.
		</li>
	</ul>
</section>

<section>
	<h2>Staff Characteristics</h2>
	<p>
		We employ both permanent and outsourced staff, hiring local employees based on country
		regulations. Specific roles requiring expertise are supported through a training center that
		plans and defines course syllabi for skill development and replacement training.
	</p>
</section>

<section>
	<h2>Product Delivery Procedure</h2>
	<p>
		Using aircraft as the primary tool, we invest in our own fleet and collaborate with alliances to
		provide seamless pre-flight, in-flight, and post-flight services. We utilize technology and
		communication networks to ensure information accessibility for both clients and staff.
	</p>
</section>
