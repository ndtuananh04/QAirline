<section>
	<h2>Company Profile</h2>
	<p>
		Thai Airways International Public Co., Ltd is a national enterprise subordinated to the Ministry
		of Transport. It is a public company registered in The Securities Exchange of Thailand since
		1991 with the Ministry of Finance as a dominant shareholder at more than 50%.
	</p>
	<p>
		We operate the airlines business that transports passengers, goods, parcels, and postal through
		air transportation via Thai major cities to the primary cities around the world. From scheduled
		flights to charter flights and through our alliances, with Suvarnabhumi Airport as the main hub
		for all flight routes.
	</p>
	<p>
		Other than airlines business, which is our main business, we also operate other related
		businesses to support our airfreight services:
	</p>
	<ul>
		<li>Passenger service, product service, pre-flight and post-flight preparation</li>
		<li>In-flight meals and beverages service</li>
		<li>Aircraft maintenance service</li>
		<li>Flight administrator service</li>
		<li>Duty-free products and souvenir merchandise</li>
		<li>Travel and traveling support service</li>
		<li>Flight service attendants training service (Joint venture)</li>
		<li>Workforce provider service (Joint venture)</li>
		<li>Aircraft fuel service (Joint venture)</li>
		<li>Seat booking and reservation service (Joint venture)</li>
	</ul>
	<p>
		Our main revenue is from passenger and goods transportation, accounting for 95%, while the
		remaining 5% comes from supporting enterprises.
	</p>
</section>

